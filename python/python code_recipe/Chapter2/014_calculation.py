# 算術演算子
'''
Syntax
x + y 足し算
x - y 引き算
x * y 掛け算
x/ y 割り算
x // y 商
x % y 余剰
-x 符号反転
x ** y 冪乗
'''

x = 100
y = 3

# 足し算
a = x + y
print(a)

# 引き算
b = x - y
print(b)

# 掛け算
c = x * y
print(c)

# 割り算
d = x / y
print(d)

# 商
e = x // y
print(e)

# 余剰
f = x % y
print(f)

# 符号反転
g = -x
print(g)

# 冪乗
h = x ** y
print(h)

