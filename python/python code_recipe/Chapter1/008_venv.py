# venvを使ってPythonの仮想環境を使いたい
'''
Syntax
python -m venv 環境構築先のパス -> 仮想環境の構築

仮想環境に切り替える
Windowsコマンドプロンプト -> 仮想環境ディレクトリ配下の¥Script¥activate.batを実行
Windows PowerShell -> 仮想環境ディレクトリ配下のScript/Activate.ps1を実行
Unix系 -> 仮想環境ディレクトリ配下のbin/activateをsourceコマンドで読み込み

仮想環境を抜ける
Windowsコマンドプロンプト -> 仮想環境ディレクトリ配下の¥Script¥deactivate.batを実行
Windows PowerShell -> deactivateコマンドを実行
Unix系 -> deactivateコマンドを実行
'''